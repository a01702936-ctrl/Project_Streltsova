#создать список А на 10 случайных элементов. Создать список В из чисел списка А,кратных 4 и оканчивающихся на 2. Найти количество 
#чисел списка В. 
import random

A = [random.randint(1, 100) for _ in range(10)]

B = [x for x in A if x % 4 == 0 and x % 10 == 2]

count = len(B)

print("Список А:", A)
print("Список В (кратные 4 и оканчиваются на 2):", B)
print("Количество чисел в списке В:", count)
